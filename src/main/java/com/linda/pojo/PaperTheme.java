package com.linda.pojo;

public class PaperTheme {
    private Integer paperId;

    private Integer firstThemeId;

    private Integer secondThemeId;

    public Integer getPaperId() {
        return paperId;
    }

    public void setPaperId(Integer paperId) {
        this.paperId = paperId;
    }

    public Integer getFirstThemeId() {
        return firstThemeId;
    }

    public void setFirstThemeId(Integer firstThemeId) {
        this.firstThemeId = firstThemeId;
    }

    public Integer getSecondThemeId() {
        return secondThemeId;
    }

    public void setSecondThemeId(Integer secondThemeId) {
        this.secondThemeId = secondThemeId;
    }
}